package Entidade.Conta.Exception;

/**
 *
 * @author fagner
 */
public class ContaDespesaException extends Exception {

    /**
     * Creates a new instance of
     * <code>ContaReceitaException</code> without detail message.
     */
    public ContaDespesaException() {
    }

    /**
     * Constructs an instance of
     * <code>ContaReceitaException</code> with the specified detail message.
     *
     * @param msg the detail message.
     */
    public ContaDespesaException(String msg) {
        super(msg);
    }
}
